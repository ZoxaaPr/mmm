const catchAsyncErrors = require("../middleware/catchSyncError");
const Order = require("../model/order");
const Product = require("../model/product");
const ApiFeatures = require("../utils/apiFeatures");
const ErrorHander = require("../utils/errorHandler");

exports.newOrder = catchAsyncErrors(async (req, res, next) => {
  const {
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
  } = req.body;

  const order = await Order.create({
    shippingInfo,
    orderItems,
    paymentInfo,
    itemsPrice,
    taxPrice,
    shippingPrice,
    totalPrice,
    paidAt: Date.now(),
    user: req.user._id,
  });

  //console.log(paidAt);

  res.status(201).json({
    success: true,
    order,
  });
});

exports.getSinglOrder = catchAsyncErrors(async (req, res, next) => {
  const order = await Order.findById(req.params.id).populate(
    "user",
    "name email"
  );

  if (!order) {
    return next(new ErrorHander("Order is not found", 404));
  }
  res.status(201).json({
    success: true,
    order,
  });
});

